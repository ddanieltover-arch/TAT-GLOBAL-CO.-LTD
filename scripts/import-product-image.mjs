/**
 * Convert and save a product photo as optimized WebP.
 *
 * Usage:
 *   node scripts/import-product-image.mjs <product-slug> <path-to-image>
 *
 * Example:
 *   node scripts/import-product-image.mjs jasmine-rice-thai-hom-mali "./photo.png"
 */
import {existsSync} from 'node:fs';
import {readFile, writeFile} from 'node:fs/promises';
import {basename, join} from 'node:path';
import {fileURLToPath} from 'node:url';
import {products} from '../lib/products.ts';

const MAX_WIDTH = 1200;
const WEBP_QUALITY = 82;

const root = join(fileURLToPath(new URL('.', import.meta.url)), '..');
const outDir = join(root, 'public', 'images', 'products');
const manifestPath = join(root, 'lib', 'product-image-manifest.ts');

async function main() {
  const [slug, inputPath] = process.argv.slice(2);

  if (!slug || !inputPath) {
    console.error('Usage: node scripts/import-product-image.mjs <product-slug> <path-to-image>');
    process.exit(1);
  }

  const product = products.find((item) => item.slug === slug);
  if (!product) {
    console.error(`Unknown slug "${slug}". Valid slugs:`);
    for (const item of products) console.error(`  - ${item.slug}`);
    process.exit(1);
  }

  const resolvedInput = join(process.cwd(), inputPath);
  if (!existsSync(resolvedInput)) {
    console.error(`File not found: ${resolvedInput}`);
    process.exit(1);
  }

  let sharp;
  try {
    sharp = (await import('sharp')).default;
  } catch {
    console.error('Install sharp first: npm install sharp --save-dev');
    process.exit(1);
  }

  const before = await readFile(resolvedInput);
  const outPath = join(outDir, `${slug}.webp`);

  const out = await sharp(before)
    .rotate()
    .resize({width: MAX_WIDTH, height: MAX_WIDTH, fit: 'inside', withoutEnlargement: true})
    .webp({quality: WEBP_QUALITY, effort: 4})
    .toBuffer();

  await writeFile(outPath, out);

  const slugs = new Set(products.filter((item) => existsSync(join(outDir, `${item.slug}.webp`))).map((item) => item.slug));
  slugs.add(slug);

  const manifest = `/**
 * Auto-generated by scripts/import-product-image.mjs — do not edit manually.
 * Slugs listed here have a dedicated WebP at public/images/products/{slug}.webp
 */
export const productImageSlugs = new Set<string>([
${[...slugs].sort().map((s) => `  ${JSON.stringify(s)},`).join('\n')}
]);
`;

  await writeFile(manifestPath, manifest);

  console.log(`Product: ${product.name}`);
  console.log(`Input:   ${basename(resolvedInput)} (${(before.length / 1024).toFixed(1)} KB)`);
  console.log(`Output:  public/images/products/${slug}.webp (${(out.length / 1024).toFixed(1)} KB)`);
  console.log(`Manifest: ${slugs.size} product image(s) registered`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
